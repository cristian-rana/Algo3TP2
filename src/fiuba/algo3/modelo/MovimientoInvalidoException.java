package fiuba.algo3.modelo;

public class MovimientoInvalidoException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public MovimientoInvalidoException() {
		super();
	}

	public MovimientoInvalidoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MovimientoInvalidoException(String arg0) {
		super(arg0);
	}

	public MovimientoInvalidoException(Throwable arg0) {
		super(arg0);
	}

	
}
