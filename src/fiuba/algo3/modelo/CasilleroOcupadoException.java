package fiuba.algo3.modelo;

public class CasilleroOcupadoException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public CasilleroOcupadoException() {
		super();
	}

	public CasilleroOcupadoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CasilleroOcupadoException(String arg0) {
		super(arg0);
	}

	public CasilleroOcupadoException(Throwable arg0) {
		super(arg0);
	}
	
}
